import mongoose from "mongoose"
import jwt from "jsonwebtoken"
import bcrypt from "bcrypt"
import dotenv from "dotenv"

const userSchema = new mongoose.Schema(
    {
        username:{
            type:String,
            required:true,
            unique:true,
            lowercase:true,
            trim:true,
            index:true
        },
        email:{
            type:String,
            required:true,
            unique:true,
            lowercase:true,
            trim:true,
        },
        fullName:{
            type:String,
            required:true,
            trim:true,
            index:true

        },
        avatar: {
            type: String,
            required:true,
        },
        coverImage:{
            type:String,
        },
        watchHistory:[
            {
                type:mongoose.Schema.Types.ObjectId,
                ref:"Video"
            }
        ],
        password:{
            type:String,
            required:[true,'Password is required.']
        },
        refreshToken:{
            type:String
        }
    },{timestamps:true}
);

dotenv.config({
    path:'../.env'
})

userSchema.pre("save", async function(){
    
    //we'll hash only if the password gets changed wont run at each run
    if (!this.isModified("password")){
        return;
    }
    
    this.password=await bcrypt.hash(this.password,10);
    

})
 
userSchema.methods.isPasswordCorrect = async function(password){
    return await bcrypt.compare(password,this.password); //this.password is the encrypted/hashed pass and the normal password is the one user inputs
}

userSchema.methods.generateAccessToken = function(){
    return jwt.sign(
        {
            _id:this._id,
            email:this.email,
            username:this.username,
            fullName:this.fullName
        },
        process.env.ACCESS_TOKEN_SECRET,
        {
            expiresIn:process.env.ACCESS_TOKEN_EXPIRY
        }
    )

}

userSchema.methods.generateRefreshToken = function(){
    return jwt.sign(
        {
            _id:this._id,
        },
        process.env.REFRESH_TOKEN_SECRET,
        {
            expiresIn:process.env.REFRESH_TOKEN_EXPIRY
        }
    )

}
export const User = mongoose.model("User",userSchema);